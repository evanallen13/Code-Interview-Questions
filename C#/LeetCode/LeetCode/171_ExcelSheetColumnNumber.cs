using System;
using System.Collections.Generic;
using System.Text;

namespace LeetCode.Easy
{
    class _171_ExcelSheetColumnNumber
    {
        public static int TitleToNumber(string s)
        {
            char[] charArr = s.ToCharArray();
            int index = 0;

            for(int i = 0; i < charArr.Length; i++)
            {
                index = 3;
            }

            return index;

        }
    }
}
